import type { MetadataRoute } from 'next';

export default function robots(): MetadataRoute.Robots {
    return {
        rules: [
            {
                userAgent: '*',
                allow: '/',
                disallow: ['/dashboard/', '/api/', '/admin/', '/seed/', '/debug-auth/', '/login'],
            },
        ],
        sitemap: 'https://pertuto.com/sitemap.xml',
    };
}
